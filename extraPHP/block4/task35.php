<?php
// Ввод размеров стола
echo "Введите размеры стола a и b (через пробел, a > b): ";
$line = trim(fgets(STDIN));
list($a, $b) = explode(' ', $line);
$a = intval($a);
$b = intval($b);

if ($a <= $b || $a <= 0 || $b <= 0) {
    echo "Ошибка: необходимо, чтобы a > b и оба числа были положительными.\n";
    exit;
}

// Ввод размеров карточных прямоугольников
echo "Введите размеры карточных прямоугольников c и d (через пробел, c > d): ";
$line2 = trim(fgets(STDIN));
list($c, $d) = explode(' ', $line2);
$c = intval($c);
$d = intval($d);

if ($c <= $d || $c <= 0 || $d <= 0) {
    echo "Ошибка: необходимо, чтобы c > d и оба числа были положительными.\n";
    exit;
}

// Расчет при размещении длинной стороной вдоль большей стороны стола
$count1 = (int)floor($a / $c) * (int)floor($b / $d);

// Расчет при размещении повернутых прямоугольников
$count2 = (int)floor($a / $d) * (int)floor($b / $c);

// Вывод результата
echo "При размещении длинной стороной вдоль большей стороны стола можно разместить: $count1 карточек.\n";
echo "При размещении повернутыми —: $count2 карточек.\n";

if ($count1 > $count2) {
    echo "Больше карточек поместится при размещении длинной стороной вдоль большей стороны.\n";
} elseif ($count2 > $count1) {
    echo "Больше карточек поместится при размещении повернутыми.\n";
} else {
    echo "Количество карточек одинаковое при обоих способах размещения.\n";
}