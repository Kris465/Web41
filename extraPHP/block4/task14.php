<?php

$a = 1.0; 
$b = -3.0;
$c = 2.0;


$D = $b * $b - 4 * $a * $c;

if ($D > 0) {

    $sqrtD = sqrt($D);
    $x1 = (-$b + $sqrtD) / (2 * $a);
    $x2 = (-$b - $sqrtD) / (2 * $a);
    echo "Корни уравнения:\n";
    echo "x1 = " . $x1 . "\n";
    echo "x2 = " . $x2 . "\n";
} elseif ($D == 0) {
 
  
    echo "Уравнение имеет один вещественный корень.\n";
} else {
    echo "Уравнение не имеет вещественных корней.\n";
}
?>