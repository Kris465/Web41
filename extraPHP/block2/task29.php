<?php
$n = (int)readline("Введите число n (10 ≤ n ≤ 999): ");

if ($n < 10 || $n > 999 || ($n / 10) % 10 == 0) {
    echo "Ошибка: число должно быть в диапазоне от 10 до 999 и десятки не должны быть равны нулю.\n";
    exit;
}

$found = false; 
for ($x = 100; $x <= 999; $x++) {
    $a = (int)($x / 100);
    $b = (int)(($x % 100) / 10); 
    $c = $x % 10; 

    $newNumber = ($b * 10) + $c; 
    $result = ($b * 100) + $newNumber; 

    if ($result == $n) {
        echo "Найдено число x: " . $x . "\n";
        $found = true;
    }
}

if (!$found) {
    echo "Число x не найдено для заданного условия.\n";
}
?>